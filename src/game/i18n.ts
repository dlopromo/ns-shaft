const DICTIONARIES: Record<Locale, Dictionary> = { ja: JA, "zh-Hant": ZH_HANT, en: EN };
let activeLocale: Locale = "ja";

export function setLocale(locale: Locale): void { activeLocale = locale; }
export function getLocale(): Locale { return activeLocale; }

export function t(
  key: TranslationKey,
  values: Record<string, string | number> = {},
  locale: Locale = activeLocale
): string {
  return Object.entries(values).reduce(
    (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
    DICTIONARIES[locale][key]
  );
}
